// an enum holds a set of named members
// it can be considered as a collection or grouping of costants
// the enum's name can be used as a type in locations where any of its members' usage is expected
// enum members are by default given a numeric value, 0 indexed auto incremented
enum Category {
  Food,
  Entertainment,
  Housing,
  Utilities,
  Transportation,
  Misc,
}

export default Category;
